package com.myoneday.myapp.entity;

public class ClassVO {

	private String className;
	private String classDescribe;
	private String classLike;
	private String categoryName;
	
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getClassDescribe() {
		return classDescribe;
	}
	public void setClassDescribe(String classDescribe) {
		this.classDescribe = classDescribe;
	}
	public String getClassLike() {
		return classLike;
	}
	public void setClassLike(String classLike) {
		this.classLike = classLike;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	
}
